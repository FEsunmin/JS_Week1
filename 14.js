// falsy한 값, truthy한 값
// 조건으로 사용할 수 있는 값

// falsy한 값 리스트
if (0) {
    console.log("hello");
}

if ("") {
    console.log("hello");
}

if (null) {
    console.log("hello");
}

if (undefined) {
    console.log("hello");
}

if (NaN) {
    console.log("hello");
}

if (false) {
    console.log("hello");
}

// truthy한 값
if (true) {
    console.log("hello");
}